package com.ZG.model;

public class stockInfo {
	private String stockId;
	private String stockName;
	private String stockPrice;
	private String stockLoc;
	private String stockComment;
	
	public String getStockId() {
		return stockId;
	}
	public void setStockId(String stockId) {
		this.stockId = stockId;
	}
	public String getStockName() {
		return stockName;
	}
	public void setStockName(String stockName) {
		this.stockName = stockName;
	}
	public String getStockPrice() {
		return stockPrice;
	}
	public void setStockPrice(String stockPrice) {
		this.stockPrice = stockPrice;
	}
	public String getStockLoc() {
		return stockLoc;
	}
	public void setStockLoc(String stockLoc) {
		this.stockLoc = stockLoc;
	}
	public String getStockComment() {
		return stockComment;
	}
	public void setStockComment(String stockComment) {
		this.stockComment = stockComment;
	}	
}
